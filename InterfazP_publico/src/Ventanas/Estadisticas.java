    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
// Contar clientes
    File carpetaClientes = new File("Datos_cliente");
    if (carpetaClientes.exists() && carpetaClientes.isDirectory()) {
        int cantidadClientes = carpetaClientes.listFiles().length;
        lblClientes.setText("Clientes registrados: " + cantidadClientes);
    } else {
        lblClientes.setText("Clientes registrados: 0");
    }

    // Procesar productos
    File carpetaProductos = new File("DatosProductos");
    Map<String, Integer> ventasPorProducto = new HashMap<>();
    Map<String, String> nombrePorCodigo = new HashMap<>();

    if (carpetaProductos.exists() && carpetaProductos.isDirectory()) {
        File[] archivos = carpetaProductos.listFiles();

        for (File archivo : archivos) {
            try {
                List<String> lineas = Files.readAllLines(archivo.toPath());
                String codigo = "", nombre = "";
                int ventas = 0;

                for (String linea : lineas) {
                    if (linea.startsWith("Codigo:")) {
                        codigo = linea.replace("Codigo:", "").trim();
                    } else if (linea.startsWith("Nombre:")) {
                        nombre = linea.replace("Nombre:", "").trim();
                    } else if (linea.startsWith("Ventas:")) {
                        ventas = Integer.parseInt(linea.replace("Ventas:", "").trim());
                    }
                }

                if (!codigo.isEmpty()) {
                    ventasPorProducto.put(codigo, ventas);
                    nombrePorCodigo.put(codigo, nombre);
                }
            } catch (IOException | NumberFormatException ex) {
                ex.printStackTrace();
            }
        }

        // Mostrar datos en tabla
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(new String[]{"CÃ³digo", "Nombre", "Ventas"});

        ventasPorProducto.entrySet().stream()
            .sorted((a, b) -> b.getValue().compareTo(a.getValue())) // de mayor a menor
            .forEach(entry -> {
                String codigo = entry.getKey();
                String nombre = nombrePorCodigo.get(codigo);
                int ventas = entry.getValue();
                modelo.addRow(new Object[]{codigo, nombre, ventas});
            });

        tablaEstadisticas.setModel(modelo);
    }        
        
    }//GEN-LAST:event_jButton1ActionPerformed

