   private void EditarProductos(String editt) {
    File archivo = new File("DatosProductos/" + editt + ".txt");

    if (!archivo.exists()) {
        JOptionPane.showMessageDialog(null, "El archivo del producto no existe.");
        return;
    }

    try {
        // Leer todas las líneas del archivo
        List<String> lineas = Files.readAllLines(archivo.toPath());

        // Pedir los nuevos valores
        String nuevoPrecio = JOptionPane.showInputDialog("Ingrese el nuevo precio:");
        if (nuevoPrecio == null) {
            JOptionPane.showMessageDialog(null, "Edición cancelada. No se realizaron cambios.");
            return;
        }

        String nuevoStock = JOptionPane.showInputDialog("Ingrese el nuevo stock:");
        if (nuevoStock == null) {
            JOptionPane.showMessageDialog(null, "Edición cancelada. No se realizaron cambios.");
            return;
        }

        boolean actualizoPrecio = false;
        boolean actualizoStock = false;
        boolean encontroLineaActualizacion = false;

        // Reemplazar datos y buscar si ya existe la línea de actualización
        for (int i = 0; i < lineas.size(); i++) {
            if (lineas.get(i).startsWith("Precio:")) {
                lineas.set(i, "Precio: " + nuevoPrecio);
                actualizoPrecio = true;
            } else if (lineas.get(i).startsWith("Stock:")) {
                lineas.set(i, "Stock: " + nuevoStock);
                actualizoStock = true;
            } else if (lineas.get(i).startsWith("Última actualización:")) {
                encontroLineaActualizacion = true;
                // Actualizar la línea con nueva fecha
                String fecha = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(new Date());
                lineas.set(i, "Última actualización: " + fecha);
            }
        }

        // Si no existía línea de actualización, agregarla al final
        if (!encontroLineaActualizacion) {
            String fecha = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(new Date());
            lineas.add("Última actualización: " + fecha);
        }

        // Escribir de nuevo el archivo
        Files.write(archivo.toPath(), lineas, StandardOpenOption.TRUNCATE_EXISTING);

        if (actualizoPrecio || actualizoStock) {
            JOptionPane.showMessageDialog(null, "Producto actualizado correctamente.");
        } else {
            JOptionPane.showMessageDialog(null, "No se encontraron los campos Precio o Stock para actualizar.");
        }

    } catch (IOException e) {
        JOptionPane.showMessageDialog(null, "Error al editar el producto: " + e.getMessage());
    }
}
        
 
    
