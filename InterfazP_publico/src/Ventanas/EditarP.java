 private void buscarcodigoPorcodigooP(String codigoUnico) {
    File archivo = new File("DatosProductos/" + codigoUnico + ".txt");
    System.out.println("Buscando en: " + archivo.getAbsolutePath());

    if (!archivo.exists()) {
        JOptionPane.showMessageDialog(null, "El archivo del producto no existe");
        return;
    }

    try {
        // Sin especificar codificación (usa la predeterminada del sistema)
        List<String> lineas = Files.readAllLines(archivo.toPath());

        StringBuilder codigoEncontrado = new StringBuilder();
        boolean encontrado = false;
        boolean leyendo = false;

        for (String linea : lineas) {
            System.out.println("Línea: " + linea);

            // Cuidado con los acentos, asegúrate de que diga "Codigo" sin tilde
            if (linea.trim().contains("Codigo: " + codigoUnico)) {
                leyendo = true;
                encontrado = true;
            }

            if (leyendo) {
                codigoEncontrado.append(linea).append("\n");
                if (linea.contains("-----------------------------")) {
                    break;
                }
            }
        }

        if (encontrado) {
            JOptionPane.showMessageDialog(null, "Producto encontrado:\n\n" + codigoEncontrado.toString());
        } else {
            JOptionPane.showMessageDialog(null, "No se encontró ningún producto con ese código.");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error al leer el archivo: " + e.getMessage());
    }
}
    
  
    
